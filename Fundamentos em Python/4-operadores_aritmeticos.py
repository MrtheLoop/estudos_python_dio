# Precedência dos operandos

# Na matemática
 #   Na matemática existe uma regra que indica quais operandos devem ser executados primeiro. Isso é útil pois ao analisar uma expressão, a depender da ordem das operações, o valor pode ser diferentes: 
# A definição indica a seguinte ordem como a correta:
# - Parêntesis
# Expoêntes
# Multiplicações e divisões (da esquerda para a direita)
# Somas e subtrações (da Esquerda para a direita)

# EX:

print(10-5*2)
#>>> 0

print ((10-5)*2)
#>>> 10

print(10**2*2)
#>>>200

print(10**(2*2))
#>>>10000

print(10/2*4)
#>>>20.0