#Os operadores aritiméticos executam operações matemáticas como adição, subtração com operandos

#Os caracteres de operação são
# + Soma
# - Subtração
# * Multiplicação
# / Divisão
# // Divisão inteira
# % Módulo (Resto da Divisão)
# ** Exponenciação


#Adição
print(1+1)

#subtração
print(10-2)

#Multiplicação
print(4*3)

#Divisão
print(12/3)

#Divisão inteira
