# Conhecendo os operadores de comparação

#O que são?

#São operadores utilizados para comparar dois valores

# Igualdade

saldo = 450
saque = 200

print(saldo == saque)
#>>>False

# Diferença

print(saldo != saque)
#>>>True

# Maior que / Maior ou igual

print(saldo > saque)
#>>>True

print(saldo >= saque)
#>>>True