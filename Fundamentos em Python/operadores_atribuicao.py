# Operadores de Atribuição

# Vale lembrar que ra cada operação ser feita individualmente, é preciso comentar as linhas que estão abaixo da operação desejada

# São operadores utilizados para definir o valor inicial ou sobrescrever o valor de uma variável

#Atribuição Simples

saldo = 500

print(saldo)
#>>>500

# Atribuição com adição

saldo = 500
saldo += 200

print(saldo)
#>>>700

# Saldo com subtração

saldo = 500
saldo -= 100

print(saldo)
#>>>400

# Saldo com multiplicação
saldo = 500
saldo *= 2

print(saldo)
#>>>1000

# Atribuição com divisão

saldo = 500
saldo /= 5

print(saldo)
#>>> 100.0

saldo = 500
saldo //= 5

print(saldo)
#>>>100

# Atribuição com módulo

saldo = 500
saldo %= 480

print(saldo)
#>>>20

# Atribuição com exponenciação

saldo = 80
saldo **= 2

print(saldo)
#>>>6400